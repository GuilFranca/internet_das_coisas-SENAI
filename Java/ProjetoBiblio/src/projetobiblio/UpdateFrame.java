    private void PesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PesquisarActionPerformed
        // TODO add your handling code here:

        try {

            UsuarioDAO usuarioDao = new UsuarioDAO();

            int idBuscado = Integer.parseInt(inputId.getText());

            Usuario usuario = usuarioDao.buscarUsurioPorId(idBuscado);

            if (usuario != null) {

                System.out.println("Usu√°rio encontrado: ");
                System.out.println("ID: " + usuario.getId());
                System.out.println("Nome: " + usuario.getNome());
                System.out.println("Email: " + usuario.getEmail());
                System.out.println("Telefone: " + usuario.getTelefone());
                System.out.println("Tipo: " + usuario.getTipo_usuario());

                inputEditNome.setText(usuario.getNome());
                inputEditEmail.setText(usuario.getEmail());
                inputEditTelefone.setText(usuario.getTelefone());
                inputEditTipoUsuario.setText(usuario.getTipo_usuario());

                alertEditar.setText("Usu√°rio encontrado");
                
            } else {
                System.out.println("Usu√°rio com ID " + idBuscado + " n√£o encontrado");
                alertEditar.setText("Usu√°rio com ID " + idBuscado + " n√£o encontrado");
            }

        } catch (SQLException e) {
            System.out.println("ERRO ao buscar usu√°rio: " + e.getMessage());
        }

    }//GEN-LAST:event_PesquisarActionPerformed

