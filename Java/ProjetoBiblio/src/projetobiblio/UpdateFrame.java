    private void EditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditarActionPerformed
        // TODO add your handling code here:
        
        try {
        
            UsuarioDAO dao = new UsuarioDAO();
            
            Usuario usuarioAtualizado = new Usuario();
            
            int idBuscado = Integer.parseInt(inputId.getText());
            
            usuarioAtualizado.setId(idBuscado);
            usuarioAtualizado.setNome(inputEditNome.getText());
            usuarioAtualizado.setEmail(inputEditEmail.getText());
            usuarioAtualizado.setTelefone(inputEditTelefone.getText());
            usuarioAtualizado.setTipo_usuario(inputEditTipoUsuario.getText());
            
            dao.atualizarUsuario(usuarioAtualizado);
            
            alertEditar.setText("Atualiza√ß√£o Concluida");
            
        } catch (SQLException e) {
            System.out.println("ERRO na opera√ß√£o: " + e.getMessage());
            alertEditar.setText("ERRO na opera√ß√£o: " + e.getMessage());
        } finally {
            
        }
        
    }//GEN-LAST:event_EditarActionPerformed

